                    Dans cette lettre (ce message), je désire t'aider à entrer dans une relation personnelle avec JÉSUS, ce qui te permettra, par la grâce de Dieu, de répondre en toute confiance par un "OUI" à chacune des questions posées ci-dessus.

                    La première chose que j'aimerais te dire c'est que Dieu est saint et juste. L'homme par contre est pécheur. La condition pécheresse de l'homme se manifeste de plusieurs manières : par exemple, il fait ce qu'il sait qu'il ne devrait pas faire. La plupart des gens mentent, certains commettent l'adultère ou la fornication, d'autres sont pleins d'envie ou cupides, d'autres volent de petites ou de grandes choses, d'autres font des commérages, d'autres sont orgueilleux et arrogants; certains usent de tromperie, d'autres corrompent les autres ou se laissent corrompre, d'autres haïssent telle ou telle personne; certains lisent des livres et des revues immoraux, etc.

                    La Bible dit que:"tous ont péché et sont privés de la gloire de Dieu."
                    Même les gens qui n'ont pas péché ouvertement ont péché dans leur coeur. D'après Jésus- Christ, si tu regardes une fille ou un garçon, un homme ou une femme avec convoitise, tu es déjà coupable d'immoralité avec elle ou avec lui dans ton coeur (Matthieu 5:28), et si tu hais quelqu'un, tu as déjà commis un meurtre dans ton coeur. Donc les péchés qui sont commis en pensées, en paroles ou en actes, sont tous graves devant Dieu. Il n'y a personne sur terre, qui n'ait jamais péché. Certains ont péché plus que d'autres, mais puisque tous ont péché, tous sont coupables devant Dieu, toi aussi.

                    Le péché sépare l'homme de Dieu, puisqu'il ne peut exister de relation entre un Dieu saint et des pécheurs. Le péché a créé un fossé entre Dieu et l'homme. Bien que l'homme ait essayé de combler le fossé de plusieurs manières, par exemple par les prières, en allant à l'église et en faisant beaucoup de ce qu'il considère commede bonnes actions, le fossé demeure. Ceci parce que le niveau de justice de Dieu est si haut que tout ce que l'homme fait par lui-même est insuffisant devant Dieu. En fait, Dieu voit toutes nos bonnes oeuvres comme des haillons sales. Elles sentent mauvais devant Lui parce que même dans le meilleur des cas, ces bonnes oeuvres sont corrompues par quelque motif égoïste ou impur. La Bible dit que "nul ne sera justifié devant Dieu par lesoeuvres de la loi" (Romains 3:20).

                    Le péché ne sépare pas seulement l'homme de Dieu. Il a aussi un salaire. Ce salaire est la mort: la séparation éternelle d'avec Dieu en enfer. Le péché se paie aussi par les brisements de coeur, et toute la souffrance que le péché engendre maintenant, bien que cette souffrance actuelle ne soit rien en comparaison avec l'agonie de l'enfer qui sera le sort de ceux qui rejettent Jésus-Christ.

                    Bien que tous les êtres humains soient pécheurs et méritent à juste titre le jugement et la punition de Dieu, Dieu néanmoins nous aime. Il nous a tant aimés qu'Il a envoyé Son Fils unique JÉSUS-CHRIST, pour qu'Il vienne et meure sur la croix à notre place. Dieu a mis sur Jésus-Christ tous nos péchés passés, présents et futurs, et Il a puni le péché une fois pour toutes sur la croix.

                    Jésus est mort là où tu aurais dû mourir. Il a versé Son sang innocent pour que les pécheurs soient pardonnés. Il a établi un pont par-dessus le fossé qui séparait l'homme de Dieu et a ainsi ouvert une nouvelle voie par laquelle le pécheur peut être réconcilié avec Dieu.

                    Jésus a fait tout cela sur la Croix, et Il est ressuscité des morts, comme preuve que Dieu était satisfait de Son oeuvre en faveur des pécheurs. Dès lors, Il vit à jamais pour pardonner à tous les pécheurs qui voudront se repentir de leurs péchés et Le recevoir comme leur Sauveur. Il veut te pardonner maintenant et te donner une nouvelle naissance, et aussi écrire ton nom dans le livre de la vie éternelle.

